package Package_for_while;
public class EX3_1 {
    public static void main(String[] args) {
        
        // 변수 초기화
        int n = 1;


        // 1 ~ 3 까지 반복하고,  4인 경우 거짓으로 종료 !
        while (n < 4) { 
            System.out.println(n);
            n ++;
        }

        // 반복문 탈출시 실행
        System.out.println("END");

        
    }    
}
